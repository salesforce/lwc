const VOID_ELEMENT_NAMES = new Set([
    'area',
    'base',
    'br',
    'col',
    'command',
    'embed',
    'hr',
    'img',
    'input',
    'keygen',
    'link',
    'meta',
    'param',
    'source',
    'track',
    'wbr',
]);

export function isVoidElement(name: string): boolean {
    return VOID_ELEMENT_NAMES.has(name);
}
